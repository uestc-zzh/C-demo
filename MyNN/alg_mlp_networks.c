void output_csv(LayerData *input_data){
    FILE* file = fopen("output.csv", "a");  // "a" 表示以追加模式打开文件
    if (file == NULL) {
        printf("无法打开文\n");
        return;
    }

    // 将数组内容逐个写入CSV文件中
    for (int i = 0; i < input_data->size; i++) {
        if (i > 0) {
            fprintf(file, ",");  // 用逗号分隔每个数组元素
        }
        fprintf(file, "%f", input_data->data[i]);
    }
    fprintf(file, "\n");  // 换行

    fclose(file);  // 关闭文件
}

