static ClassResult result_classification(LayerData *hidden_layer_output)
{
    uint16_t i = 0, j = 0;
    for (i = 0; i < hidden_layer_output->size; i++) {
        if (hidden_layer_output->data[i] > hidden_layer_output->data[j]) {
            j = i;
        }
    }
    return (ClassResult)j;
}

